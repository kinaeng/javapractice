public class ANDORNOT {
    public static void main(String[] args) {
        /*
         * AND && และ
         * Or || หรือ
         * NOt ! ไม่
         */
        /*
         * AND
         * int a=10 , b =20;
         * boolean c = (a==b); //false
         * boolean d = (a<b); //true
         * System.out.println(c&&d);
         */
        /*
         * OR
         * int a = 10, b = 20;
         * boolean c = (a == b); // false
         * boolean d = (a < b); // true
         * System.out.println(c || d);
         */
        // NOT
        int a = 10, b = 20;
        boolean c = (a == b); // false
        
        System.out.println(!c);
    }
}
